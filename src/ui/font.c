static void draw_char(SDL_Renderer *r, char c, int x, int y, SDL_Color col) {
    if (c < 32 || c > 127) {
        c = ' ';
    }
    const uint8_t *g = GLYPHS[c - 32];
    SDL_SetRenderDrawColor(r, col.r, col.g, col.b, 255);
    for (int row = 0; row < 7; row++) {
        for (int bit = 0; bit < 5; bit++) {
            if (g[row] & (1 << (4 - bit))) {
                SDL_Rect px = { x + bit * FONT_SCALE, y + row * FONT_SCALE, FONT_SCALE, FONT_SCALE };
                SDL_RenderFillRect(r, &px);
            }
        }
    }
}

void font_draw_string(SDL_Renderer *r, const char *s, int x, int y, SDL_Color col) {
    int cx = x;
    while (*s) {
        draw_char(r, *s, cx, y, col);
        cx += FONT_CSTRIDE;
        s++;
    }
}

int font_str_px_w(const char *s) {
    return (int)strlen(s) * FONT_CSTRIDE;
}
